#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t	alloc;
	char	*blob;

	alloc = nmemb * size;
	if (alloc != 0 && alloc / nmemb != size)
		return (NULL);
	blob = malloc(alloc);
	if (blob == NULL)
		return (blob);
	ft_bzero(blob, alloc);
	return (blob);
}
