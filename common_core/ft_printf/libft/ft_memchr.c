#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*r;

	r = s;
	while (n-- > 0)
	{
		if (*r == (unsigned char) c)
			return ((void *) r);
		++r;
	}
	return (NULL);
}
