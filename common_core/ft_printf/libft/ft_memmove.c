#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char		*d;
	const unsigned char	*s;

	if (dest == NULL && src == NULL)
		return (NULL);
	d = dest;
	s = src;
	if (dest < src)
	{
		while (n-- > 0)
			*d++ = *s++;
		return (dest);
	}
	while (n-- > 0)
		d[n] = s[n];
	return (dest);
}
