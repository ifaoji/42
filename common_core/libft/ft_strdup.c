#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*cpy;
	char	*tmp;

	cpy = malloc(ft_strlen(src) + 1);
	if (cpy == NULL)
		return (NULL);
	tmp = cpy;
	while (*src)
		*tmp++ = *src++;
	*tmp = '\0';
	return (cpy);
}
