#include "push_swap.h"

bool	ft_is_sorted(t_node *stack)
{
	t_node	*current;

	if (stack == NULL || stack->next == NULL)
		return (true);
	current = stack;
	while (current->next)
	{
		if (current->value > current->next->value)
			return (false);
		current = current->next;
	}
	return (true);
}
